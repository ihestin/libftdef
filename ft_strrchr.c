#include <string.h>

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i] != '\0')
		i++;
	i++;
	while (i > 0)
	{
		i--;
		if (s[i] == c)
			return ((char *)&s[i]);
	}
	return (0);
}
